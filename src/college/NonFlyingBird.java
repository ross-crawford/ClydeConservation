package college;

public class NonFlyingBird extends Animal {

	private String habitat;
	private String optLandSpeed;
	
	public NonFlyingBird() {
		
	}

	public String getHabitat() {
		return habitat;
	}

	public void setHabitat(String habitat) {
		this.habitat = habitat;
	}

	public String getOptLandSpeed() {
		return optLandSpeed;
	}

	public void setOptLandSpeed(String optLandSpeed) {
		this.optLandSpeed = optLandSpeed;
	}
	
	
}
