package college;

public class Mammal extends Animal {

	private String animalMate;
	private String gaveBirth;
	
	public Mammal() {
		
	}

	public String getAnimalMate() {
		return animalMate;
	}

	public void setAnimalMate(String animalMate) {
		this.animalMate = animalMate;
	}

	public String getGaveBirth() {
		return gaveBirth;
	}

	public void setGaveBirth(String gaveBirth) {
		this.gaveBirth = gaveBirth;
	}
	
}
