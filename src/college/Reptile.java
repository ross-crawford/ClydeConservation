package college;

public class Reptile extends Animal {

	private String environment;
	private String cageTemp;
	
	public Reptile() {
		
	}

	public String getEnvironment() {
		return environment;
	}

	public void setEnvironment(String environment) {
		this.environment = environment;
	}

	public String getCageTemp() {
		return cageTemp;
	}

	public void setCageTemp(String cageTemp) {
		this.cageTemp = cageTemp;
	}
	
}
