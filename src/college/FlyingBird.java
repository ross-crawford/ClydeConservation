package college;

public class FlyingBird extends Animal {

	private String wingSpan;
	private String optFlightSpeed;
	
	public FlyingBird() {
		
	}

	public String getWingSpan() {
		return wingSpan;
	}

	public void setWingSpan(String wingSpan) {
		this.wingSpan = wingSpan;
	}

	public String getOptFlightSpeed() {
		return optFlightSpeed;
	}

	public void setOptFlightSpeed(String optFlightSpeed) {
		this.optFlightSpeed = optFlightSpeed;
	}
	
	
}
